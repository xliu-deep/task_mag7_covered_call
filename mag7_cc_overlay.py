#  CC OVERLAY ENGINE
# ═══════════════════════════════════════════════════════════════════════════════

def run_cc_overlay(data, initial_capital, target_delta, risk_free_rate=0.04):
    entry_price = data[0][1]
    lots = int(initial_capital / (entry_price * 100))
    shares = lots * 100
    stock_cost = shares * entry_price
    leftover_cash = initial_capital - stock_cost
    n_months = len(data)

    cc_cash = 0.0
    total_premiums = 0.0
    total_assignment_cost = 0.0
    times_called = 0
    times_expired = 0
    monthly_log = []

    for month, open_px, close_px, iv in data:
        T = 1.0 / 12.0
        strike = strike_for_delta(open_px, target_delta, T, risk_free_rate, iv)
        if strike <= open_px:
            strike = open_px + (0.50 if open_px < 50 else 2.50 if open_px < 200 else 5.00)

        otm_pct = (strike - open_px) / open_px * 100
        premium_per_sh = black_scholes_call(open_px, strike, T, risk_free_rate, iv)
        month_premium = premium_per_sh * shares
        cc_cash += month_premium
        total_premiums += month_premium

        assigned = close_px >= strike
        if assigned:
            cost = (close_px - strike) * shares
            cc_cash -= cost
            total_assignment_cost += cost
            times_called += 1
        else:
            cost = 0.0
            times_expired += 1

        stock_value = shares * close_px + leftover_cash
        total_value = stock_value + cc_cash

        monthly_log.append({
            "month": month, "open_px": open_px, "close_px": close_px,
            "strike": strike, "otm_pct": otm_pct, "premium": month_premium,
            "assigned": assigned, "assign_cost": cost,
            "cc_net_month": month_premium - cost, "cc_cash_cumul": cc_cash,
            "stock_value": stock_value, "total_value": total_value,
        })

    exit_price = data[-1][2]
    bh_value = shares * exit_price + leftover_cash
    bh_return = (bh_value - initial_capital) / initial_capital * 100
    net_cc_income = total_premiums - total_assignment_cost
    total_value = bh_value + net_cc_income
    total_return = (total_value - initial_capital) / initial_capital * 100
    cc_overlay_pct = net_cc_income / initial_capital * 100

    return {
        "shares": shares, "entry_price": entry_price, "exit_price": exit_price,
        "bh_value": bh_value, "bh_return_pct": bh_return,
        "total_premiums": total_premiums, "total_assignment_cost": total_assignment_cost,
        "net_cc_income": net_cc_income, "total_value": total_value,
        "total_return_pct": total_return, "cc_overlay_pct": cc_overlay_pct,
        "times_called": times_called, "times_expired": times_expired,
        "monthly_log": monthly_log, "n_months": n_months,
    }


