# ═══════════════════════════════════════════════════════════════════════════════
#  STRATEGY 1: BUY AND HOLD
# ═══════════════════════════════════════════════════════════════════════════════

def buy_and_hold(data, cap):
    entry = data[0][1]
    lots = int(cap / (entry * 100))
    shares = lots * 100
    cash = cap - shares * entry
    exit_px = data[-1][2]
    final = cash + shares * exit_px
    ret = (final - cap) / cap * 100
    # max drawdown
    peak = cap
    max_dd = 0
    for _, _, close, _ in data:
        val = cash + shares * close
        peak = max(peak, val)
        dd = (peak - val) / peak * 100
        max_dd = max(max_dd, dd)
    return {"final": final, "return_pct": ret, "shares": shares,
            "entry": entry, "exit": exit_px, "max_dd": max_dd}

# ═══════════════════════════════════════════════════════════════════════════════
#  STRATEGY 2: B&H + CC (traditional: re-buy after assignment)
# ═══════════════════════════════════════════════════════════════════════════════

def bh_plus_cc(data, cap, delta, r=0.04):
    cash = cap
    shares = 0
    total_prem = 0
    n_called = 0
    n_expired = 0
    log = []

    for i, (mo, opn, cls, iv) in enumerate(data):
        # buy shares if we have none
        if shares == 0:
            lots = int(cash / (opn * 100))
            if lots == 0:
                log.append((mo, opn, cls, 0, 0, 0, "SKIP", cash))
                continue
            shares = lots * 100
            cash -= shares * opn

        T = 1.0 / 12.0
        strike = strike_for_delta(opn, delta, T, r, iv)
        if strike <= opn:
            strike = opn + (0.50 if opn < 50 else 2.50 if opn < 200 else 5.00)

        prem_sh = bs_call(opn, strike, T, r, iv)
        prem = prem_sh * shares
        cash += prem
        total_prem += prem

        assigned = cls >= strike
        if assigned:
            cash += shares * strike
            n_called += 1
            port = cash
            log.append((mo, opn, cls, strike, prem, shares, "CALLED", port))
            shares = 0
        else:
            n_expired += 1
            port = cash + shares * cls
            log.append((mo, opn, cls, strike, prem, shares, "hold", port))

    final = cash + shares * data[-1][2]
    ret = (final - cap) / cap * 100

    # max drawdown
    peak = cap
    max_dd = 0
    for entry in log:
        val = entry[7]
        peak = max(peak, val)
        dd = (peak - val) / peak * 100
        max_dd = max(max_dd, dd)

    return {"final": final, "return_pct": ret, "total_prem": total_prem,
            "n_called": n_called, "n_expired": n_expired,
            "max_dd": max_dd, "log": log}

# ═══════════════════════════════════════════════════════════════════════════════
#  STRATEGY 3: CC OVERLAY (always hold, track CC P&L separately)
# ═══════════════════════════════════════════════════════════════════════════════

def cc_overlay(data, cap, delta, r=0.04):
    entry = data[0][1]
    lots = int(cap / (entry * 100))
    shares = lots * 100
    leftover = cap - shares * entry

    cc_cash = 0.0
    total_prem = 0.0
    total_asn_cost = 0.0
    n_called = 0
    peak = cap
    max_dd = 0

    for mo, opn, cls, iv in data:
        T = 1.0 / 12.0
        strike = strike_for_delta(opn, delta, T, r, iv)
        if strike <= opn:
            strike = opn + (0.50 if opn < 50 else 2.50 if opn < 200 else 5.00)

        prem = bs_call(opn, strike, T, r, iv) * shares
        cc_cash += prem
        total_prem += prem

        if cls >= strike:
            cost = (cls - strike) * shares
            cc_cash -= cost
            total_asn_cost += cost
            n_called += 1

        val = shares * cls + leftover + cc_cash
        peak = max(peak, val)
        dd = (peak - val) / peak * 100
        max_dd = max(max_dd, dd)

    exit_px = data[-1][2]
    bh_val = shares * exit_px + leftover
    net_cc = total_prem - total_asn_cost
    final = bh_val + net_cc
    ret = (final - cap) / cap * 100

    return {"final": final, "return_pct": ret, "net_cc": net_cc,
            "total_prem": total_prem, "asn_cost": total_asn_cost,
            "n_called": n_called, "max_dd": max_dd}


