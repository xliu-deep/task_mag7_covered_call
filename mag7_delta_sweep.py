"""
Delta Sweep: Fine-grained search for optimal delta per stock
═══════════════════════════════════════════════════════════════

Uses CC OVERLAY model with delta range 0.03 to 0.50
to find the TRUE peak for each stock.

Key question: Does the optimal delta keep improving at lower values,
or is there a real peak/inflection point?
"""

import math
from scipy.stats import norm

def bs_call(S, K, T, r, sigma):
    if T <= 0 or sigma <= 0:
        return max(S - K, 0)
    d1 = (math.log(S / K) + (r + 0.5 * sigma**2) * T) / (sigma * math.sqrt(T))
    d2 = d1 - sigma * math.sqrt(T)
    return S * norm.cdf(d1) - K * math.exp(-r * T) * norm.cdf(d2)

def strike_for_delta(S, td, T, r, sigma):
    d1 = norm.ppf(td)
    K = S * math.exp((r + 0.5 * sigma**2) * T - d1 * sigma * math.sqrt(T))
    if S < 50: return round(K * 2) / 2
    elif S < 200: return round(K / 2.5) * 2.5
    else: return round(K / 5) * 5

