#  CC OVERLAY ENGINE
# ═══════════════════════════════════════════════════════════════════════════════

def cc_overlay(data, cap, delta, r=0.04):
    entry = data[0][1]
    lots = int(cap / (entry * 100))
    shares = lots * 100
    leftover = cap - shares * entry

    cc_cash = 0.0
    total_prem = 0.0
    total_asn = 0.0
    n_called = 0

    for _, opn, cls, iv in data:
        T = 1.0 / 12.0
        strike = strike_for_delta(opn, delta, T, r, iv)
        if strike <= opn:
            strike = opn + (0.50 if opn < 50 else 2.50 if opn < 200 else 5.00)

        prem = bs_call(opn, strike, T, r, iv) * shares
        cc_cash += prem
        total_prem += prem

        if cls >= strike:
            cost = (cls - strike) * shares
            cc_cash -= cost
            total_asn += cost
            n_called += 1

    net = total_prem - total_asn
    bh_val = shares * data[-1][2] + leftover
    final = bh_val + net
    bh_ret = (bh_val - cap) / cap * 100
    tot_ret = (final - cap) / cap * 100
    cc_pct = net / cap * 100
    avg_otm = 0
    count = 0
    for _, opn, _, iv in data:
        T = 1.0/12.0
        s = strike_for_delta(opn, delta, T, r, iv)
        if s > opn:
            avg_otm += (s - opn) / opn * 100
            count += 1
    avg_otm = avg_otm / max(count, 1)

    return {
        "bh_ret": bh_ret, "tot_ret": tot_ret, "cc_pct": cc_pct,
        "prem": total_prem, "asn": total_asn, "net": net,
        "called": n_called, "avg_otm": avg_otm,
    }


